package W3resource;

public class IfElsePractice {
	public static void main(String[] args) {
		
		if(10>20) {
			
			System.out.println("Inside if 1");
		
		}else if(10>20) {
			
			System.out.println("Inside else if 2");
		
		}else if(10>20) {
			
			System.out.println("Inside else if 3");
		
		}
		
		else {
		
			System.out.println("Inside else 4");
		
		}
	}

}
